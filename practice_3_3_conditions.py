name = input("Enter name: ")

# IF ONLY
if name == 'wakas':
    print("Name is equal")

# IF OR ELSE
if name == 'wakas':
    print("Name is equal")
else:
    print("Not Equal")

# IF OR ELSE
if name == 'wakas':
    print("Name is equal")
else:
    print("Not Equal")

# IF ELIF or ELSE
if name == 'wakas':
    print("Wakas")
elif name == 'ikram':
    print("Ikram")
elif name == 'junaid':
    print("junaid")
else:
    print("Not -available")

